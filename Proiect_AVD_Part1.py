# 5. ESTIMAREA PENTRU ECHIPELE CARE ÎNCĂ LIPSESC
# =========================================================
def estimeaza_valoare_dupa_rank(rank):
    # Formulă bazată pe corelația Rank/Valoare.
    val = np.exp(-0.0425 * rank + 20.49)
    # Punem o limită minimă de 2 milioane Euro (pt echipele foarte slabe) și un maxim realist
    return np.clip(val, 2_000_000, 1_800_000_000)

