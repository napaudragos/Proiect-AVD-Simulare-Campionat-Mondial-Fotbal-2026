async def messi_scrape_match_details(page, url, search_term, toate_meciurile):
    print(f"\n[{url}] --> STARTING SCRAPE")

    rand_meci = {
        "URL Meci": url,
        "Echipa Căutată": search_term.title()  # Format estetic frumos
    }

    try:
        # --- PASUL 1: Găsirea echipelor din pagină ---
        await page.wait_for_selector('[class*="duelParticipant"]', timeout=15000)
        home_team_raw = await page.locator('.duelParticipant__home, .duelParticipant_home').first.inner_text()
        away_team_raw = await page.locator('.duelParticipant__away, .duelParticipant_away').first.inner_text()

        # =========================================================
        # FUNCȚIE DE CURĂȚARE EXTREMĂ (Elimină textul parazit FIFA și diacriticele complexe)
        # =========================================================
        def normalizeaza_nume_fotbal(text):
            if not text: return ""
            text = text.lower()
            # Eliminăm textul cu FIFA și tot ce urmează după el (ex: \nfifa\n: 30.)
            text = re.sub(r'[\n\r]*fifa.*', '', text)
            # Curățăm orice cifre sau semne de punctuație rămase accidentale
            text = re.sub(r'[^a-z\s]', '', text)
            # Înlocuim absolut toate tipurile de diacritice (sedile, virgule, accente)
            replacements = {
                'ă': 'a', 'â': 'a', 'î': 'i',
                'ș': 's', 'ş': 's',
                'ț': 't', 'ţ': 't',
                'â': 'a', 'î': 'i', 'ț': 't'
            }
            for de_sters, cu_ce_inlocuim in replacements.items():
                text = text.replace(de_sters, cu_ce_inlocuim)
            return text.strip()

        # Îndepărtăm textul parazit "FIFA: XX" pentru a salva curat numele adversarului în CSV
        home_team_clean_name = re.sub(r'[\n\r]*fifa.*', '', home_team_raw).strip()
        away_team_clean_name = re.sub(r'[\n\r]*fifa.*', '', away_team_raw).strip()

        # Normalizăm textele pentru compararea logică a algoritmului
        home_lower = normalizeaza_nume_fotbal(home_team_raw)
        away_lower = normalizeaza_nume_fotbal(away_team_raw)
        search_lower = normalizeaza_nume_fotbal(search_term)

        # Dicționarul de traduceri oficial fără diacritice (pentru potriviri perfecte la caractere)
        traduceri_simple = {
            "spain": "spania", "france": "franta", "germany": "germania",
            "italy": "italia", "england": "anglia", "netherlands": "olanda",
            "switzerland": "elvetia", "sweden": "suedia", "croatia": "croatia",
            "turkey": "turcia", "belgium": "belgia", "scotland": "scotia",
            "austria": "austria", "denmark": "danemarca", "norway": "norvegia",
            "portugal": "portugalia", "canada": "canada", "mexico": "mexic",
            "united states": "sua", "usa": "sua", "argentina": "argentina",
            "brazil": "brazilia", "colombia": "columbia", "ecuador": "ecuador",
            "paraguay": "paraguay", "uruguay": "uruguay", "japan": "japonia",
            "iran": "iran", "iraq": "irak", "jordan": "iordania",
            "south korea": "coreea de sud", "qatar": "qatar",
            "saudi arabia": "arabia saudita", "australia": "australia",
            "uzbekistan": "uzbekistan", "new zealand": "noua zeelanda",
            "algeria": "algeria", "tunisia": "tunisia", "ghana": "ghana",
            "senegal": "senegal", "ivory coast": "coasta de fildes",
            "cote d ivoire": "coasta de fildes", "dr congo": "rd congo",
            "congo dr": "rd congo", "cape verde": "capul verde",
            "morocco": "maroc", "panama": "panama", "curacao": "curacao",
            "haiti": "haiti", "bosnia and herzegovina": "bosnia"
        }

        termen_tradus_ro = traduceri_simple.get(search_lower, search_lower)

        # LOGICA REPARATĂ: Verificăm potrivirea pe textul normalizat complet
        is_home = (search_lower in home_lower) or (termen_tradus_ro in home_lower)
        is_away = (search_lower in away_lower) or (termen_tradus_ro in away_lower)

        if not is_home and not is_away:
            print(f"[{url}] AVERTISMENT CRITIC: Nu am putut identifica dacă {search_term} e Home sau Away!")
            print(f"Pe ecran text brut: HOME='{home_team_raw}', AWAY='{away_team_raw}'")
            is_home = False  # Fallback

        # Alocăm corect rolul meciului și extragem numele curat al adversarului
        if is_home:
            rand_meci["Rol Echipă"] = "Acasă (Home)"
            rand_meci["Adversar"] = away_team_clean_name
        else:
            rand_meci["Rol Echipă"] = "Deplasare (Away)"
            rand_meci["Adversar"] = home_team_clean_name

        # --- EXTRAGEREA SCORULUI ---
        print(f"[{url}] Extrag scorul meciului...")
        try:
            await page.wait_for_selector('.detailScore__wrapper', timeout=5000)
            scoruri_spans = await page.locator('.detailScore__wrapper span').all_inner_texts()

            if len(scoruri_spans) >= 3:
                scor_home = int(scoruri_spans[0].strip())
                scor_away = int(scoruri_spans[2].strip())

                print(f"[{url}] Scor găsit: {home_team_clean_name} {scor_home} - {scor_away} {away_team_clean_name}")

                if is_home:
                    goluri_marcate = scor_home
                    goluri_primite = scor_away
                else:
                    goluri_marcate = scor_away
                    goluri_primite = scor_home

                rand_meci["Goluri Marcate"] = goluri_marcate
                rand_meci["Goluri Primite"] = goluri_primite

                if goluri_marcate > goluri_primite:
                    rand_meci["Rezultat Final"] = "Victorie"
                elif goluri_marcate < goluri_primite:
                    rand_meci["Rezultat Final"] = "Înfrângere"
                else:
                    rand_meci["Rezultat Final"] = "Egal"
            else:
                print(f"[{url}] Structură de scor invalidă: {scoruri_spans}")
        except Exception:
            print(f"[{url}] Scorul nu a putut fi parsat (meci anulat/amânat).")
            rand_meci["Goluri Marcate"] = "N/A"
            rand_meci["Goluri Primite"] = "N/A"
            rand_meci["Rezultat Final"] = "N/A"

        # --- PASUL 2: Navigarea la tab-ul Statistici ---
        stats_tab = page.get_by_role("tab", name="Statistici", exact=True)

        try:
            # Forțăm scraperul să aștepte tab-ul de statistici până la 30 de secunde în caz de lag
            print(f"[{url}] Aștept încărcarea tab-ului Statistici (Max 30 secunde)...")
            await stats_tab.wait_for(state="visible", timeout=5000)
        except Exception:
            print(f"[{url}] Tab-ul 'Statistici' nu a apărut în 30s. Trecem la următorul meci.")
            toate_meciurile.append(rand_meci)
            return

        await stats_tab.click()
        print(f"[{url}] Click efectuat. Pauză de 5 secunde pentru randarea tabelelor...")
        await page.wait_for_timeout(5000)

        print(f"[{url}] Aștept selectorul final de date...")
        await page.wait_for_selector('[data-testid="wcl-statistics-category"]', timeout=15000)

        # --- PASUL 4: Extragerea datelor statistice ---
        stats_extrase = await page.locator('[data-testid="wcl-statistics"]').evaluate_all("""
            (rows, isHome) => {
                return rows.map(row => {
                    let categoryEl = row.querySelector('[data-testid="wcl-statistics-category"]');
                    let category = categoryEl ? categoryEl.innerText.trim() : 'Necunoscut';

                    let homeValEl = row.querySelector('[class*="wcl-homeValue"]');
                    let awayValEl = row.querySelector('[class*="wcl-awayValue"]');

                    let homeVal = homeValEl ? homeValEl.innerText.trim() : '0';
                    let awayVal = awayValEl ? awayValEl.innerText.trim() : '0';

                    return {
                        category: category,
                        value: isHome ? homeVal : awayVal
                    };
                });
            }
        """, is_home)

        print(f"[{url}] S-au extras {len(stats_extrase)} categorii de statistici.")

        # --- PASUL 5: Parsare cu Regex și salvare ---
        for stat in stats_extrase:
            cat = stat['category']
            val = str(stat['value']).replace('\n', ' ')

            match = re.search(r'(\d+)%\s*\(\s*(\d+)\s*/\s*(\d+)\s*\)', val)
            if match:
                procentaj = match.group(1)
                reusite = match.group(2)
                totale = match.group(3)
                adauga_coloana_unica(rand_meci, f"{cat} (%)", procentaj)
                adauga_coloana_unica(rand_meci, f"{cat} reusite", reusite)
                adauga_coloana_unica(rand_meci, f"{cat} totale", totale)
            else:
                adauga_coloana_unica(rand_meci, cat, val)

        toate_meciurile.append(rand_meci)
        print(f"[{url}] Meci înregistrat cu succes.")

    except Exception as e:
        print(f"[{url}] Eroare generală la procesarea paginii: {e}")

    print(f"[{url}] --> FINALIZAT SCRAPE\n")

