async def scrape_multiple_teams(lista_echipe, max_items_per_team=5):
    pw = None
    browser = None

    # AICI ESTE SECRETUL: Această listă comună va aduna date de la TOATE echipele
    dataset_comun = []

    try:
        pw = await async_playwright().start()
        browser = await pw.chromium.launch(headless=True)
        page = await browser.new_page()

        # Iterăm prin fiecare echipă din lista ta
        for index, search_term in enumerate(lista_echipe, 1):
            print(f"\n{'='*50}")
            print(f"[{index}/{len(lista_echipe)}] ÎNCEPEM CĂUTAREA PENTRU: '{search_term.upper()}'")
            print(f"{'='*50}")

            try:
                # Mergem pe pagina principală pentru a reseta căutarea
                await page.goto("https://www.flashscore.ro/", timeout=90000)

                # Căutăm echipa
                await page.click('.searchIcon', timeout=60000)
                await page.fill('.searchInput__input', search_term, timeout=60000)

                await page.wait_for_selector('.searchResults__section', timeout=30000)
                await page.locator('.searchResult').first.click(timeout=15000)

                # Navigăm la rezultate
                rezultate_tab_selector = 'a[title="Rezultate"]'
                await page.wait_for_selector(rezultate_tab_selector, timeout=30000)
                await page.click(rezultate_tab_selector, timeout=15000)

                # Extragem link-urile meciurilor
                await page.wait_for_selector('.eventRowLink', timeout=30000)
                match_links = await page.locator('.eventRowLink').evaluate_all(
                    "elements => elements.map(el => el.href)"
                )

                links_to_scrape = match_links[:max_items_per_team]
                print(f"Am găsit {len(links_to_scrape)} meci(uri) pentru {search_term}.")

                # Intrăm pe fiecare meci
                for i, link in enumerate(links_to_scrape, 1):
                    print(f"\n[{search_term}] Navigăm la meciul {i}/{len(links_to_scrape)}...")
                    await page.goto(link, timeout=60000)

                    # Pasăm dataset_comun mai departe
                    await scrape_match_details(page, link, search_term, dataset_comun)

            except Exception as team_error:
                # Dacă pică scraping-ul la o echipă (ex: "panama"), prindem eroarea
                # și scriptul va continua cu următoarea echipă din listă!
                print(f"!!! Eroare la procesarea echipei '{search_term}': {team_error} !!!")
                print("Trecem la următoarea echipă...")
                continue

        # --- FINAL DE TOT: CREARE DATAFRAME COMUN ---
        if dataset_comun:
            df = pd.DataFrame(dataset_comun)
            print(f"\n{'*'*50}")
            print(f"SCRAPING FINALIZAT! Am extras un total de {len(df)} meciuri.")
            print(f"{'*'*50}")

            # Afișăm o scurtă previzualizare
            print(df.head())

            # Salvăm totul într-un singur fișier
            nume_fisier = "dataset_campionatul_mondial.csv"
            #nume_fisier = "messi_echipe_reparate.csv"
            df.to_csv(nume_fisier, index=False, encoding='utf-8-sig')
            print(f"\nToate datele au fost salvate cu succes în: {nume_fisier}")
        else:
            print("\nNu s-au extras date pentru nicio echipă.")

    except Exception as e:
        print(f"Eroare critică generală: {e}")
    finally:
        if browser:
            await browser.close()
        if pw:
            await pw.stop()

