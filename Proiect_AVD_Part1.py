def adauga_coloana_unica(dictionar, nume_baza, valoare):
    nume_coloana = nume_baza
    counter = 1
    while nume_coloana in dictionar:
        nume_coloana = f"{nume_baza}_{counter}"
        counter += 1
    dictionar[nume_coloana] = valoare

