async def scrape_match_details(page, url, search_term, toate_meciurile):
    print(f"\n[{url}] --> STARTING SCRAPE")

    rand_meci = {
        "URL Meci": url,
        "Echipa Căutată": search_term
    }

    try:
        # --- PASUL 1: Găsirea echipelor și a SCORULUI ---
        await page.wait_for_selector('[class*="duelParticipant"]', timeout=15000)
        home_team_name = await page.locator('.duelParticipant__home, .duelParticipant_home').first.inner_text()
        away_team_name = await page.locator('.duelParticipant__away, .duelParticipant_away').first.inner_text()

        is_home = search_term.lower() in home_team_name.lower()
        rol_echipa = "Acasă (Home)" if is_home else "Deplasare (Away)"
        rand_meci["Rol Echipă"] = rol_echipa

        # --- NOU: EXTRAGEREA SCORULUI ---
        print(f"[{url}] Extrag scorul meciului...")
        try:
            # Așteptăm div-ul care conține scorul
            await page.wait_for_selector('.detailScore__wrapper', timeout=5000)

            # all_inner_texts() ne va da o listă cu textul fiecărui span (ex: ['1', '-', '1'] sau ['2', '-', '0'])
            scoruri_spans = await page.locator('.detailScore__wrapper span').all_inner_texts()

            # Ne asigurăm că am găsit exact 3 elemente și curățăm textul de posibile spații libere
            if len(scoruri_spans) >= 3:
                scor_home = int(scoruri_spans[0].strip())
                scor_away = int(scoruri_spans[2].strip())

                print(f"[{url}] Scor găsit: {home_team_name.strip()} {scor_home} - {scor_away} {away_team_name.strip()}")

                # Alocăm golurile și determinăm rezultatul în funcție de echipa noastră
                if is_home:
                    goluri_marcate = scor_home
                    goluri_primite = scor_away
                else:
                    goluri_marcate = scor_away
                    goluri_primite = scor_home

                # Salvăm golurile
                rand_meci["Goluri Marcate"] = goluri_marcate
                rand_meci["Goluri Primite"] = goluri_primite

                # Calculăm Rezultatul
                if goluri_marcate > goluri_primite:
                    rand_meci["Rezultat Final"] = "Victorie"
                elif goluri_marcate < goluri_primite:
                    rand_meci["Rezultat Final"] = "Înfrângere"
                else:
                    rand_meci["Rezultat Final"] = "Egal"
            else:
                print(f"[{url}] Nu s-a putut parsa scorul din elementele: {scoruri_spans}")
        except Exception as scor_err:
            print(f"[{url}] ATENȚIE: Nu s-a putut extrage scorul (posibil meci anulat/fără scor afișat).")
            rand_meci["Goluri Marcate"] = "N/A"
            rand_meci["Goluri Primite"] = "N/A"
            rand_meci["Rezultat Final"] = "N/A"
        # --------------------------------

        # --- PASUL 2: Navigarea la tab-ul Statistici ---
        stats_tab = page.get_by_role("tab", name="Statistici", exact=True)

        if await stats_tab.count() == 0:
            print(f"[{url}] ATENȚIE: Tab-ul 'Statistici' nu există pentru acest meci! (Trecem la următorul)")
            # Salvăm totuși meciul în listă! Chiar dacă nu are statistici, avem măcar scorul.
            toate_meciurile.append(rand_meci)
            return

        await stats_tab.wait_for(state="visible", timeout=10000)
        await stats_tab.click()

        print(f"[{url}] Click efectuat. Aștept 2 secunde încărcarea datelor...")
        await page.wait_for_timeout(2000)

        print(f"[{url}] Aștept să apară textul categoriilor ([data-testid='wcl-statistics-category'])...")
        await page.wait_for_selector('[data-testid="wcl-statistics-category"]', timeout=15000)

        # --- PASUL 4: Extragerea datelor cu JavaScript ---
        stats_extrase = await page.locator('[data-testid="wcl-statistics"]').evaluate_all("""
            (rows, isHome) => {
                return rows.map(row => {
                    let categoryEl = row.querySelector('[data-testid="wcl-statistics-category"]');
                    let category = categoryEl ? categoryEl.innerText.trim() : 'Necunoscut';

                    let homeValEl = row.querySelector('[class*="wcl-homeValue"]');
                    let awayValEl = row.querySelector('[class*="wcl-awayValue"]');

                    let homeVal = homeValEl ? homeValEl.innerText.trim() : '0';
                    let awayVal = awayValEl ? awayValEl.innerText.trim() : '0';

                    return {
                        category: category,
                        value: isHome ? homeVal : awayVal
                    };
                });
            }
        """, is_home)

        print(f"[{url}] S-au extras {len(stats_extrase)} categorii cu succes!")

        # --- PASUL 5: Procesarea datelor (Regex & Curățare) ---
        for stat in stats_extrase:
            cat = stat['category']
            val = str(stat['value']).replace('\n', ' ')

            match = re.search(r'(\d+)%\s*\(\s*(\d+)\s*/\s*(\d+)\s*\)', val)
            if match:
                procentaj = match.group(1)
                reusite = match.group(2)
                totale = match.group(3)
                adauga_coloana_unica(rand_meci, f"{cat} (%)", procentaj)
                adauga_coloana_unica(rand_meci, f"{cat} reusite", reusite)
                adauga_coloana_unica(rand_meci, f"{cat} totale", totale)
            else:
                adauga_coloana_unica(rand_meci, cat, val)

        toate_meciurile.append(rand_meci)
        print(f"[{url}] Meci ADĂUGAT în listă cu scor și statistici.")

    except Exception as e:
        print(f"[{url}] EROARE CRITICĂ: {type(e).__name__} - {e}")

    print(f"[{url}] --> FINALIZAT SCRAPE\n")



