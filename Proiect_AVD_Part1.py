# Dicționar centralizat pentru a găsi potrivirea perfectă
TRADUCERI_FLASHSCORE = {
    "france": ["franta", "franța", "france"],
    "croatia": ["croatia", "croația", "croatia"],
    "ivory coast": ["coasta de fildes", "coasta de fildeș", "ivory coast", "cote d'ivoire"]
}

def curata_nume_simplu(text):
    if not text: return ""
    text = text.lower()
    text = re.sub(r'[\n\r]*fifa.*', '', text)
    return text.strip()

def este_echipa_noastra(nume_gasit, search_term):
    nume_gasit_clean = curata_nume_simplu(nume_gasit)
    search_term_clean = search_term.lower().strip()

    # 1. Verificare directă
    if search_term_clean in nume_gasit_clean:
        return True

    # 2. Verificare prin dicționar
    variante = TRADUCERI_FLASHSCORE.get(search_term_clean, [])
    for varianta in variante:
        if varianta in nume_gasit_clean:
            return True

    # 3. Verificare de rezervă (fără diacritice)
    nume_gasit_fara_diacritice = re.sub(r'[ăâîșşțţ]', lambda m: {'ă':'a', 'â':'a', 'î':'i', 'ș':'s', 'ş':'s', 'ț':'t', 'ţ':'t'}[m.group(0)], nume_gasit_clean)
    variante_fara_diacritice = [re.sub(r'[ăâîșşțţ]', lambda m: {'ă':'a', 'â':'a', 'î':'i', 'ș':'s', 'ş':'s', 'ț':'t', 'ţ':'t'}[m.group(0)], v) for v in variante]

    for varianta in variante_fara_diacritice:
        if varianta in nume_gasit_fara_diacritice:
            return True

    return False

async def messi_scrape_match_details_simplificat(page, url, search_term, toate_meciurile):
    print(f"\n[{url}] --> STARTING SCRAPE")
    rand_meci = {"URL Meci": url, "Echipa Căutată": search_term.title()}

    try:
        await page.wait_for_selector('[class*="duelParticipant"]', timeout=15000)
        home_team_raw = await page.locator('.duelParticipant__home, .duelParticipant_home').first.inner_text()
        away_team_raw = await page.locator('.duelParticipant__away, .duelParticipant_away').first.inner_text()

        # Determinăm clar cine e cine
        is_home = este_echipa_noastra(home_team_raw, search_term)
        is_away = este_echipa_noastra(away_team_raw, search_term)

        if not is_home and not is_away:
            print(f"[{url}] EROARE CRITICĂ: Nu am găsit '{search_term}' în '{home_team_raw}' sau '{away_team_raw}'")
            return # Sărim peste dacă e total greșit

        if is_home:
            rand_meci["Rol Echipă"] = "Acasă (Home)"
            rand_meci["Adversar"] = curata_nume_simplu(away_team_raw).title()
        else:
            rand_meci["Rol Echipă"] = "Deplasare (Away)"
            rand_meci["Adversar"] = curata_nume_simplu(home_team_raw).title()

        print(f"[{url}] Extrag scorul...")
        try:
            await page.wait_for_selector('.detailScore__wrapper', timeout=5000)
            scoruri_spans = await page.locator('.detailScore__wrapper span').all_inner_texts()

            if len(scoruri_spans) >= 3:
                scor_home = int(scoruri_spans[0].strip())
                scor_away = int(scoruri_spans[2].strip())

                if is_home:
                    rand_meci["Goluri Marcate"], rand_meci["Goluri Primite"] = scor_home, scor_away
                else:
                    rand_meci["Goluri Marcate"], rand_meci["Goluri Primite"] = scor_away, scor_home

                if rand_meci["Goluri Marcate"] > rand_meci["Goluri Primite"]:
                    rand_meci["Rezultat Final"] = "Victorie"
                elif rand_meci["Goluri Marcate"] < rand_meci["Goluri Primite"]:
                    rand_meci["Rezultat Final"] = "Înfrângere"
                else:
                    rand_meci["Rezultat Final"] = "Egal"
            else:
                rand_meci.update({"Goluri Marcate": "N/A", "Goluri Primite": "N/A", "Rezultat Final": "N/A"})
        except Exception:
            rand_meci.update({"Goluri Marcate": "N/A", "Goluri Primite": "N/A", "Rezultat Final": "N/A"})

        # STATISTICI
        stats_tab = page.get_by_role("tab", name="Statistici", exact=True)
        try:
            await stats_tab.wait_for(state="visible", timeout=30000)
        except Exception:
            print(f"[{url}] Fără Statistici. Salvăm doar scorul.")
            toate_meciurile.append(rand_meci)
            return

        await stats_tab.click()
        await page.wait_for_timeout(3000)
        await page.wait_for_selector('[data-testid="wcl-statistics-category"]', timeout=15000)

        stats_extrase = await page.locator('[data-testid="wcl-statistics"]').evaluate_all("""
            (rows) => {
                return rows.map(row => {
                    let catEl = row.querySelector('[data-testid="wcl-statistics-category"]');
                    let homeEl = row.querySelector('[class*="wcl-homeValue"]');
                    let awayEl = row.querySelector('[class*="wcl-awayValue"]');
                    return {
                        category: catEl ? catEl.innerText.trim() : 'Necunoscut',
                        home: homeEl ? homeEl.innerText.trim() : '0',
                        away: awayEl ? awayEl.innerText.trim() : '0'
                    };
                });
            }
        """)

        for stat in stats_extrase:
            cat = stat['category']
            # Alegem valoarea corectă bazat pe rolul nostru confirmat mai sus!
            val_raw = stat['home'] if is_home else stat['away']
            val = str(val_raw).replace('\n', ' ')

            match = re.search(r'(\d+)%\s*\(\s*(\d+)\s*/\s*(\d+)\s*\)', val)
            if match:
                adauga_coloana_unica(rand_meci, f"{cat} (%)", match.group(1))
                adauga_coloana_unica(rand_meci, f"{cat} reusite", match.group(2))
                adauga_coloana_unica(rand_meci, f"{cat} totale", match.group(3))
            else:
                adauga_coloana_unica(rand_meci, cat, val)

        toate_meciurile.append(rand_meci)
        print(f"[{url}] Meci înregistrat.")
    except Exception as e:
        print(f"[{url}] Eroare: {e}")

